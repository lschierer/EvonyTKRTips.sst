use 5.40.0;
use strict;
use warnings;
package Game::Evony::Covenant;
# ABSTRACT: Game::Evony support for General Covenants

=head1 SYNOPSIS

In Evony TKR, a Covenant has a Primary General and requires at least one
and up to 3 other Generals to activate.  The attributes it provides
may affect just the Primary General, or all Generals.  Thus the Covenant
only sort of belongs to the Primary General, despite that being the way
the game displays it.

=cut

1;
