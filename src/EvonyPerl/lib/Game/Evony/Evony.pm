use 5.40.0;
use strict;
use warnings;
package Game::Evony;

# ABSTRACT: Analysis tools for players of Evony TKR
=head1 SYNOPSIS

These modules is to assist players of the online game _Evony: The King's Return_.
It includes methods for reading in data files describing Evony Generals,
creating tables comparing the performance of those generals (usually as
pairs), and hopefully more over time.


This top level Game::Evony module exists primary as a placeholder to
organize the underlying modules in the namespace. It does not really
do anything on its own.
=cut

1;
